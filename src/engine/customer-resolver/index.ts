export function _seedBusinessAutoCloseDaysForTest(
  businessId: string,
  autoCloseDays: number,
): void {
  _businesses.set(businessId, { id: businessId, autoCloseDays });
}

/**
 * Backdate a conversation's closedAt to `daysAgo` days before now.
 * Use after _closeConversationForTest to place the close time outside
 * the auto_close_days window for E07/E08 scenarios.
 */
export function _setClosedAtForTest(conversationId: string, daysAgo: number): void {
  const conv = _conversations.get(conversationId);
  if (conv) {
    conv.closedAt = new Date(Date.now() - daysAgo * MS_PER_DAY);
  }
}

/**
 * Return the current primaryState of a conversation.
 * Used to assert that the old closed conversation was not mutated
 * during a re-open resolve call.
 */
export function _getConversationStateForTest(
  conversationId: string,
): { primaryState: string } | null {
  const conv = _conversations.get(conversationId);
  if (!conv) return null;
  return { primaryState: conv.primaryState };
}
